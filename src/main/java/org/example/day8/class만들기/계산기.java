package org.example.day8.class만들기;

public class 계산기 {
    public int 더하기(int n1, int n2){
        System.out.println("더하기 기능 처리");
        System.out.println(n1 + n2);
        return n1 + n2;
    }
    public void 빼기(int n1, int n2){
        System.out.println("빼기 기능 처리");
        System.out.println(n1 - n2);
    }
}
