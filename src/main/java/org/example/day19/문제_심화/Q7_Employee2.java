package org.example.day19.문제_심화;

class Q7_Employee2 {
    String name;
    String department;
    int age;

    Q7_Employee2(String name, String department, int age) {
        this.name = name;
        this.department = department;
        this.age =
                age;
    }

    public String getName() {
        return name;
    }

    public String getDepartment() {
        return department;
    }

    public int getAge() {
        return age;
    }
}
